func main() {
	line := nextLine()
	N := parseInt(line)
	t := make([]int, 4)

	for i := 0; i < N; i++ {
		line = nextLine()
		t[i] = parseInt(line)

	}

	min := 100000

	for bit := 0; bit < (1 << uint(N)); bit++ {
		asum, bsum := 0, 0

		for i := 0; i < N; i++ {
			if 1&(bit>>uint(i)) == 1 {
				asum += t[i]
			} else {
				bsum += t[i]
			}
		}

		ttime := int(math.Max(float64(asum), float64(bsum)))

		if min > ttime {
			min = ttime
		}
	}

	fmt.Println(min)
}
